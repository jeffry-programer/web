                        establecimiento comercial con el objetivo de adquirir productos o servicios para su uso
                        personal, familiar o doméstico, sin fines comerciales o de reventa. Este tipo de usuario
                        es el destinatario final de los productos o servicios y es considerado el eslabón final en
                        la cadena de consumo. El Usuario Consumidor es quien realiza consulta de productos o
                        servicios a través de la aplicación.
                    </p>

